package com.java.typeCasting;
// Narrow casting is done by placing the type in parentheses in front of the value.

public class NarrowCastingDemo {
	
	public static void main(String args[]){
		
		double typeDouble =29.2;
		
		//Converting larger type to smaller type
		int typeInt = (int) typeDouble;
		
		System.out.println(typeDouble);
		System.out.println(typeInt);

	}
	
	
	
	

}
